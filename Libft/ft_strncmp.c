#include "libft.h"

int			ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int		i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i])
	{
		if (--n == 0 || (s1[i] == '\0' && s2[i] == '\0'))
			return (0);
		i++;
	}
	return (s1[i] - s2[i]);
}
